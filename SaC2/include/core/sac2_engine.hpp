#ifndef SAC2_ENGINE_HPP
#define SAC2_ENGINE_HPP

#include <SFML/System.hpp>

#include <sac2_type.hpp>

namespace sac2
{

//////////////////////////////////////////////////////////////////////////////
//! \class Engine
//! \brief
//!     Base Engine
//! \code
//! #include <sac2.hpp>
//!
//! int main(int argc, char* argv[])
//! {
//!   sac2::Engine engine;
//!   engine.parse_options(argc, argv);
//!   return engine.run();
//! }
//! \endcode
//////////////////////////////////////////////////////////////////////////////
class Engine
{
public:
  ////////////////////////////////////////////////////////////////////////////
  //! \brief
  //!     Default constructor
  ////////////////////////////////////////////////////////////////////////////
  Engine();

  ////////////////////////////////////////////////////////////////////////////
  //! \brief
  //!     Default destructor
  ////////////////////////////////////////////////////////////////////////////
  ~Engine();

  ////////////////////////////////////////////////////////////////////////////
  //! \brief
  //!     Get the current state of the engine.
  //! \return
  //!     Current state of the engine.
  //! \see
  //!     engine_state_t
  ////////////////////////////////////////////////////////////////////////////
  state_t get_engine_state() const;

  ////////////////////////////////////////////////////////////////////////////
  //! \brief
  //!     Analyse command line arguments
  //! \param argc
  //!     Number of arguments.
  //! \param argv
  //!     Values of the arguments.
  ////////////////////////////////////////////////////////////////////////////
  void parse_options(int argc, char* argv[]);

  ////////////////////////////////////////////////////////////////////////////
  //! \brief
  //!     Start the engine.
  //! \return
  //!     Status code:
  //! \retval STATUS_SUCCESS
  //! \retval STATUS_FAILED
  ////////////////////////////////////////////////////////////////////////////
  status_t run();

  ////////////////////////////////////////////////////////////////////////////
  //! \brief
  //!     Stop the engine and quit.
  ////////////////////////////////////////////////////////////////////////////
  static void quit();

protected:
  ////////////////////////////////////////////////////////////////////////////
  //! \brief
  //!     Main loop.
  ////////////////////////////////////////////////////////////////////////////
  void loop();

  ////////////////////////////////////////////////////////////////////////////
  //! \brief
  //!    Clean the engine before quit.
  ////////////////////////////////////////////////////////////////////////////
  void cleanup();

private:
  ////////////////////////////////////////////////////////////////////////////
  //! \brief
  //!     Gives elapsed time.
  ////////////////////////////////////////////////////////////////////////////
  sf::Clock          m_clock;

  ////////////////////////////////////////////////////////////////////////////
  //! \brief
  //!     Current state of the engine.
  ////////////////////////////////////////////////////////////////////////////
  static state_t     m_engine_state;
};  // class Engine

}  // namespace sac2

#endif  //! SAC2_ENGINE_HPP
